    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        // TODO add your handling code here:
        if ( namedm.getText().isEmpty() ) {
            JOptionPane.showMessageDialog(this, "Không được để trống! Mời bạn nhập lại!");
        } else {
            try {
              
                PreparedStatement add = con.prepareStatement("insert into DanhMuc values (?,?)");
                add.setString(1, namedm.getText());
                add.setString(2, mieuta.getText());
                int row = add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Thêm danh mục thành công!");
               
                DisplayInTable();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_AddActionPerformed

