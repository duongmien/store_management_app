    private void editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editMouseClicked
        // TODO add your handling code here:
         if (iddm.getText().isEmpty() || namedm.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Thiếu thông tin chỉnh sửa");
        } else {
            try {
               
                String sql = "Update DanhMuc set TenDanhMuc=N'"+namedm.getText()+"'"+",MieuTa=N'"+mieuta.getText()+"'"+"where MaDanhMuc="+iddm.getText();
                Statement Add = con.createStatement();
                Add.executeUpdate(sql);
                DisplayInTable();
                JOptionPane.showMessageDialog(this, "Sửa Thành Công");
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_editMouseClicked

    private void BangDanhMucMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BangDanhMucMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) BangDanhMuc.getModel();
        int Myindex = BangDanhMuc.getSelectedRow();
        iddm.setText(model.getValueAt(Myindex, 0).toString());
        namedm.setText(model.getValueAt(Myindex, 1).toString());
        mieuta.setText(model.getValueAt(Myindex, 2).toString());
    }//GEN-LAST:event_BangDanhMucMouseClicked

    private void DeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteMouseClicked
        // TODO add your handling code here:
        if (iddm.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn mã danh mục để xóa");
        } else {
            try {
              
                String idnv = iddm.getText();
                String sql = "Delete from DanhMuc where MaDanhMuc=" + idnv;
                Statement Add = con.createStatement();
                Add.executeUpdate(sql);
                JOptionPane.showMessageDialog(this, "Xóa Thành Công!");
                DisplayInTable();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_DeleteMouseClicked

